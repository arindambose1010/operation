package com.ahct.preauth.vo;

public class AttachmentVO {
	private String testId;
    private String testName;
    private String filePath;
    private int fileSize;
    private String fileName;
    private String fileExtsn;
    private String fileContentType;
    private String fileReportPath;
    private String fileAttachPath;
    private String attachId;
    private String icdProcCode;
    private String caseStat;
        
	public String getTestId() {
		return testId;
	}
	public void setTestId(String testId) {
		this.testId = testId;
	}
	public String getTestName() {
		return testName;
	}
	public void setTestName(String testName) {
		this.testName = testName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public int getFileSize() {
		return fileSize;
	}
	public void setFileSize(int fileSize) {
		this.fileSize = fileSize;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileExtsn() {
		return fileExtsn;
	}
	public void setFileExtsn(String fileExtsn) {
		this.fileExtsn = fileExtsn;
	}
	public String getFileContentType() {
		return fileContentType;
	}
	public void setFileContentType(String fileContentType) {
		this.fileContentType = fileContentType;
	}
	public String getFileReportPath() {
		return fileReportPath;
	}
	public void setFileReportPath(String fileReportPath) {
		this.fileReportPath = fileReportPath;
	}
	public String getFileAttachPath() {
		return fileAttachPath;
	}
	public void setFileAttachPath(String fileAttachPath) {
		this.fileAttachPath = fileAttachPath;
	}
	public String getAttachId() {
		return attachId;
	}
	public void setAttachId(String attachId) {
		this.attachId = attachId;
	}
	public String getIcdProcCode() {
		return icdProcCode;
	}
	public void setIcdProcCode(String icdProcCode) {
		this.icdProcCode = icdProcCode;
	}
	public String getCaseStat() {
		return caseStat;
	}
	public void setCaseStat(String caseStat) {
		this.caseStat = caseStat;
	}
	
}
